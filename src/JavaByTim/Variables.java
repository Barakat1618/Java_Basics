
public class Variables {

	public static void main(String[] args) {
		//Variables are like boxes or more specifically these are the memory location created 
		//to store the values. 
		
		int myFirstNumber = (10 + 5 ) + (2*10);
		int mySecondNumber = 12;
		int myThirdNumber = (myFirstNumber * 2);
		
		int myTotal = myFirstNumber + mySecondNumber + myThirdNumber;
		
		//We can separate with underscore as well for better understanding
		int myFourthNumber = 34_56_867;
		
		System.out.println(myTotal);
		System.out.println(myFourthNumber);		
		
		
		
		
		
	}

}
